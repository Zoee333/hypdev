# Assign a single strin: “The!quick!brown!fox!jumps!over!the!lazy!dog.”
single_string = "The!quick!brown!fox!jumps!over!the!lazy!dog."

# Replace "!" with a space, then print
single_string = single_string.replace("!", " ")
print(single_string)

# Rewrite with all characters as capitals, then print
single_string = single_string.upper()
print(single_string)

# Print string in reverse
print(single_string[::-1])